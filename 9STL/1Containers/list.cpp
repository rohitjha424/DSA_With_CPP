// Learning About Lists in CPP

#include <iostream>
#include <list>
using namespace std;

int main()
{
    // Creation Of List
    list<int> myList;

    //insertion
    //insertion from back:
    myList.push_back(10);
    myList.push_back(20);
    myList.push_back(30);
    myList.push_back(40);

    //insertion fron front:
    myList.push_front(4);
    myList.push_front(3);
    myList.push_front(2);
    myList.push_front(1);

    









    return 0;
}