// Function Example for code readability

#include <iostream>
using namespace std;

void printName(string name)
{

    for (int i = 0; i < 3; i++)
    {
        cout << name << endl;
    }
}

int main()
{

    printName("Rohit");
    printName("Sharma");
    printName("Pooja");
    printName("Kohli");
    printName("Nitin");

    return 0;
}